colores = ['rojo', 'azul', 'verde', 'amarillo', 'marrón', 'lila', 'negro', 'rosa', 'blanco', 'naranja']

# Accediendo a los colores utilizando índices negativos
print(colores[-1])  # 'naranja'
print(colores[-2])  # 'blanco'
print(colores[-6])  # 'lila'
print(colores[-4])  # 'amarillo'
print(colores[-10]) # 'rojo' (esto es el primer elemento, también se puede usar colores[0])
